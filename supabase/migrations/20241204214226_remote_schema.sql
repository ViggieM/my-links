CREATE OR REPLACE FUNCTION "public"."get_parent_tags_and_siblings"("tag_ids" bigint[]) RETURNS TABLE("id" bigint, "name" "text", "parent_id" smallint)
    LANGUAGE "sql"
    AS $$
WITH RECURSIVE
  parent_tags AS (
    SELECT
      id,
      NAME,
      parent_id
    FROM
      public.tags
    WHERE
      id = ANY (tag_ids) -- Replace with your list of integers
    UNION ALL
    SELECT
      t.id,
      t.name,
      t.parent_id
    FROM
      public.tags t
      INNER JOIN parent_tags pt ON t.id = pt.parent_id
  ),
  siblings AS (
    SELECT
      t.id,
      t.name,
      t.parent_id
    FROM
      public.tags t
      INNER JOIN parent_tags pt ON t.parent_id = pt.parent_id
    WHERE
      t.id <> pt.id -- Exclude the tag itself
  )
SELECT
  id,
  NAME,
  parent_id
FROM
  parent_tags
UNION
SELECT
  id,
  NAME,
  parent_id
FROM
  siblings;
$$;


ALTER FUNCTION "public"."get_parent_tags_and_siblings"("tag_ids" bigint[]) OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."get_parent_tags_multiple"("tag_ids" bigint[]) RETURNS TABLE("id" bigint, "name" "text", "parent_id" smallint)
    LANGUAGE "sql"
    AS $$
WITH RECURSIVE parent_tags AS (
    SELECT 
        id,
        name,
        parent_id
    FROM 
        public.tags
    WHERE 
        id = ANY(tag_ids)  -- Start with the tags having the specified ids

    UNION ALL

    SELECT 
        t.id,
        t.name,
        t.parent_id
    FROM 
        public.tags t
    INNER JOIN 
        parent_tags pt ON t.id = pt.parent_id  -- Join to find parent tags
)
SELECT 
    id,
    name,
    parent_id
FROM 
    parent_tags;
$$;


ALTER FUNCTION "public"."get_parent_tags_multiple"("tag_ids" bigint[]) OWNER TO "postgres";

