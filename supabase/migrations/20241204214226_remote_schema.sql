CREATE OR REPLACE FUNCTION "public"."search_tags_by_prefix"("prefix" "text") RETURNS SETOF "public"."tags"
    LANGUAGE "plpgsql"
    AS $$
begin
  return query
  select * from tags where search_vector @@ to_tsquery(prefix || ':*');
end;
$$;


ALTER FUNCTION "public"."search_tags_by_prefix"("prefix" "text") OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."tags_search_vector_update"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$BEGIN
    NEW.search_vector := to_tsvector(
        'english',
        coalesce(NEW.name, '') || ' ' || 
        coalesce(array_to_string(NEW.alternative_names, ' '), '')
    );
    RETURN NEW;
END;$$;


